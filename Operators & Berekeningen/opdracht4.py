# Maak twee variabelen x en y met de waarden 17 en 4.

# Bereken en sla op in aparte variabelen:

# de gewone deling (/)

# de gehele deling (//)

# de restwaarde (modulo %)

# Print alle drie de resultaten in één duidelijke zin, zodat je het verschil kunt zien.

x = 17
y = 4

deel = x / y
geheel_deling = x // y
rest_waarde = x % y

print(f"De deel van de som is: {deel}, de gheel deling van de som is: {geheel_deling}, en de rest waarde is: {rest_waarde}")