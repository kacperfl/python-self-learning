# Wat je moet doen:

# Maak twee variabelen a en b met de waarden 5 en 2.

# Bereken een eerste resultaat zonder haakjes: tel b × 3 op bij a.

# Bereken een tweede resultaat met haakjes: tel eerst a + b op, en vermenigvuldig het totaal met 3.

# Print beide resultaten met een duidelijke zin, zodat je het verschil in uitkomst kunt zien.

a = 5
b = 2
eerst_resultaat = b * 3 + a
tweede_resultaat = (a + b) * 3
print(f"de resultaat van de eerste som is:{eerst_resultaat}, en van de tweede som: {tweede_resultaat:.2f}")